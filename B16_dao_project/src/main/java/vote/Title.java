package vote;

public class Title {
	private String title;
	private int size;
	
	
	
	public Title(String title, int size) {
		this.title = title;
		this.size = size;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
