package vote;

public class Info {
	private int number;
	private String info;
	private int count;
	
	
	public Info(int number, String info, int count) {
		this.number = number;
		this.info = info;
		this.count = count;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
	
	
}
